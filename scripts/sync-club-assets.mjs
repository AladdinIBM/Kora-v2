import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { ESPNAdapter } from '../shared/espn-adapter.js'
import { ESPN_ENDPOINTS, LEAGUES } from '../shared/constants.js'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const dataDirectory = path.join(root, 'data')

async function getJson(url) {
  const response = await fetch(url, {
    headers: {
      Accept: 'application/json',
      'User-Agent': 'ClubPulse asset sync/1.0',
    },
  })
  if (!response.ok) {
    throw new Error(`HTTP ${response.status} for ${url}`)
  }
  return response.json()
}

await mkdir(dataDirectory, { recursive: true })

const allTeams = []
for (const league of LEAGUES) {
  const raw = await getJson(ESPN_ENDPOINTS.teams(league.code))
  const teams = ESPNAdapter.normalizeTeams(raw, league.code)
  for (const team of teams) {
    allTeams.push({
      ...team,
      localLogoPath: null,
    })
  }
  console.log(`Synced ${teams.length} clubs for ${league.code}`)
}

allTeams.sort(
  (first, second) =>
    first.leagueCode.localeCompare(second.leagueCode) ||
    first.name.localeCompare(second.name, 'en', { sensitivity: 'base' }),
)

const generatedAt = new Date().toISOString()
const data = {
  generatedAt,
  source: LEAGUES.map((league) => ESPN_ENDPOINTS.teams(league.code)),
  teams: allTeams,
}

await writeFile(
  path.join(dataDirectory, 'bundled-teams.json'),
  `${JSON.stringify(data, null, 2)}\n`,
)
await writeFile(
  path.join(root, 'shared', 'bundled-teams.js'),
  `// Generated by scripts/sync-club-assets.mjs on ${generatedAt}\n` +
    `export const BUNDLED_TEAMS_GENERATED_AT = ${JSON.stringify(generatedAt)}\n` +
    `export const BUNDLED_TEAMS = Object.freeze(${JSON.stringify(allTeams, null, 2)})\n`,
)

console.log(`Wrote ${allTeams.length} bundled teams`)
